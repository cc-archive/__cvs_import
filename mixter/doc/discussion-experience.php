<FONT SIZE="5">Design the User Experience:</FONT><BR><BR>
<P>
The service will have several forums in different locations.  First, there
will be several forums in the main "forums" section of the site, with a few 
different common topics (Music Composition, Mixing, Editing, Equipment are
a few ideas).  A user clicking on the "forums" section will be presented with
a list of these topic-oriented forums, and by clicking on one will be 
presented with a list of the most recently active threads in that forum.
<P>
Additionally, each article will have a forum associated with it that will
be a "comments" section, to better allow content to drive discussion.
On any page showing information about an article, there will be a link to
the comments section for that article, along with the number of comments 
that have already been made.  Clicking on this link will display the 
article in full-text view, and below it its associated forum.
<P>
Finally, each group will have the option of having associated public and 
private forums (zero or one of each).  These allow users to communicate with
a group, and communicate with each other about a group.  A private forum 
will allow group members to collaborate with each other and share their 
thoughts about each others' work.
