<P>
I really like how discussions are oriented around pieces of content.  Each 
new article has an associated "forum".  That means it's easy to find discussion
about, say, a new computer game -- just look for articles about it, and read
their associated comments.
<P>
Also, the threaded and dthreaded viewing modes are pretty interesting.  Instead
of a flat format like most forum systems, in threaded mode each "main topic"
in a forum is displayed in fulltext, rather than just as a subject line.  Each 
reply to this main topic is displayed in a list below it, identified by its 
"subject" (in this case, just the first hundred or so characters of the post).
Clicking on one of these replies moves the reply up to the pane occupied by the
original "main topic".  This lets users easily navigate through thousands of
posts, when a "flat" display method (like photo.net) would become impossible
to make sense of.
<P>
Also, users have access to a powerful fulltext search of thousands of posts,
but they're limited to sixty searches per hour to prevent attacks on the site.
I think that's kinda neat.
