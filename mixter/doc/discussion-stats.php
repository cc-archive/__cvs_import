<P>
"Sticky" messages that stay at the top of the thread list are a useful feature.
This way moderators can keep something like an FAQ at the top of the threads,
so newbies have an easily-accessible way of reading the rules.   However,
some forums on Anandtech have almost too many of these messages, driving real
content that repeat users come for below the fold.
<P>
Interesting or important threads can be given special icons next to their 
subject line by a moderator.  This makes it easy to identify must-read items
(like really hot deals in the hot deals forum).
