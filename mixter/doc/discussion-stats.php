<P>
I like how Slashdot uses a threaded view but ALSO displays the fulltext of 
messages that are sub-replies (ie deep in a thread) but have been modded up
(ie a lot of moderators thought it was a useful post).  Neat trick.
<P>
It's also very easy to change your viewing settings, as there's a bar with
dropdown menus to make such changes at the top of every thread.  Much better
than burying it in a couple layers of "options" menus, if making it available at
all.
