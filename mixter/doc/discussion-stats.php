The "last comment", which provides a timestamp and author for the last post in 
thread from the top-level view of all the threads, is a nice feature.  Combined
with the fact that threads that are replied to get "juggled" to the top of the
list, it's easy for me to know which threads I should come back and read if I'm
absent for a day or two.
<P>
Other than that, however, this is pretty much bottom-of-the-barrel for 
discussion forums as far as I'm concerned.
