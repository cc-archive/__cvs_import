<FONT SIZE=5>Mixter.com</FONT>

Mixter is a community for musicians and music fans. It allows musicians to upload share music, which others can download and listen to. Songs uploaded and downloaded are protected by <A HREF="www.creativecommons.org">Creative Commons</A> licenses. Mixter has a number of community features that encourage collaboration, conversation, and information sharing. Mixter stands out in its licensing capabilities. It also includes a simple interface for artists to specify whether music they are uploading remixes or resamples other works on the site. If it does, there is an easy interface for artists and listeners to see which content has been resampled by which other content. Mixter also supports user profiles, and has a number of Friendster-esque (group network) search tools.


<H2>Features for Downloaders</H2>
<UL>
<LI>No registration to download music!</LI>
<LI>Music is available under Creative Commons licenses, so the RIAA won't come
after you for downloading it.</LI>
</UL>

<H2>Features for Artists</H2>
<UL>
<LI>Easy web interface for uploading music.</LI>
<LI>Choose a custom license to indicate what rights you want to reserve.</LI>
<LI>Indicate whose music you remix, and find out who's remixing your music.</LI>
<LI>Find new music that you can use in your works under clear licensing terms.
</UL>
